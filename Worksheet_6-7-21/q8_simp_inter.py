prin = float(input("Enter the principle: "))
rate = float(input("Enter the rate: "))
time = float(input("Enter the time: "))

SI = prin * rate * time
amount = SI / (rate * time)

print(f"The simple interest is {SI}")
print(f"The Principle amount is {amount}")

