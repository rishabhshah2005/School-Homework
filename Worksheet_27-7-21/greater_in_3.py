# Q4.WAP to find which no is greater no in 3 numbers.

n1 = int(input("Enter Number 1: "))
n2 = int(input("Enter Number 2: "))
n3 = int(input("Enter Number 3: "))

maxi = max(n1, n2, n3)

print(f"{maxi} is the greatest.")

