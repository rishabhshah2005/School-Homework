# Q5.WAP to check whether a number is divisible by 5 and 11 or not.

inp = int(input("Enter number: "))

if inp % 5 == 0 & inp % 11==0:
    print("The number is divisible by 5 and 11")

else:
    print("the number is NOT divisible by 5 and 11")


